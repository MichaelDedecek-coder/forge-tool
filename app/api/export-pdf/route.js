import { NextResponse } from "next/server";

// PDF Export Engine — serverless-compatible (puppeteer-core + @sparticuz/chromium)
// Generates vector-quality, downloadable PDFs with professional layout
export const maxDuration = 60;

export async function POST(req) {
  let browser = null;

  try {
    const chromium = (await import("@sparticuz/chromium")).default;
    const puppeteer = (await import("puppeteer-core")).default;

    const body = await req.json();
    const { reportData, fileName, language } = body;

    if (!reportData) {
      return NextResponse.json(
        { error: "No report data provided" },
        { status: 400 }
      );
    }

    console.log("[PDF Export] Starting PDF generation...");

    // Launch serverless Chromium
    browser = await puppeteer.launch({
      args: chromium.args,
      defaultViewport: { width: 1200, height: 1600, deviceScaleFactor: 2 },
      executablePath: await chromium.executablePath(),
      headless: chromium.headless,
    });

    const page = await browser.newPage();

    // Generate HTML content for PDF
    const htmlContent = generatePDFHTML(reportData, language || "en");

    // Use base64 data URL to avoid ByteString encoding issues with non-ASCII
    // characters (Czech: č, ž, ř, etc.) in page.setContent()
    const base64Html = Buffer.from(htmlContent, "utf-8").toString("base64");
    await page.goto(`data:text/html;charset=utf-8;base64,${base64Html}`, {
      waitUntil: "networkidle0",
    });
    await new Promise((resolve) => setTimeout(resolve, 1000));

    // Generate PDF with professional settings
    const pdfBuffer = await page.pdf({
      format: "A4",
      printBackground: true,
      margin: {
        top: "2cm",
        right: "1.5cm",
        bottom: "2cm",
        left: "1.5cm",
      },
      displayHeaderFooter: true,
      headerTemplate: "<div></div>",
      footerTemplate: `
        <div style="font-size: 8pt; color: #94a3b8; text-align: center; width: 100%; padding: 10px;">
          <span style="margin-right: 20px;">Generated by DataPalo AI</span>
          <span style="margin-right: 20px;">|</span>
          <span style="margin-right: 20px;">forgecreative.cz/datapalo</span>
          <span style="margin-left: 30px; color: #cbd5e1;">Page <span class="pageNumber"></span> of <span class="totalPages"></span></span>
        </div>
      `,
      preferCSSPageSize: false,
    });

    await browser.close();
    browser = null;

    console.log(
      `[PDF Export] PDF generated successfully (${(pdfBuffer.length / 1024).toFixed(0)} KB)`
    );

    // Return PDF file as downloadable attachment
    return new NextResponse(pdfBuffer, {
      status: 200,
      headers: {
        "Content-Type": "application/pdf",
        "Content-Disposition": `attachment; filename="${sanitizeFileName(fileName || "DataPalo_Report")}_${new Date().toISOString().slice(0, 10)}.pdf"`,
        "Content-Length": pdfBuffer.length.toString(),
      },
    });
  } catch (error) {
    console.error("[PDF Export] Error:", error);

    if (browser) {
      try {
        await browser.close();
      } catch (_) {}
    }

    return NextResponse.json(
      { error: error.message || "PDF generation failed" },
      { status: 500 }
    );
  }
}

// Sanitize file name for Content-Disposition header (ASCII-only required)
function sanitizeFileName(name) {
  return String(name)
    .normalize("NFD")                      // decompose: č → c + combining mark
    .replace(/[\u0300-\u036f]/g, "")       // strip combining diacritical marks
    .replace(/[^a-zA-Z0-9 _\-]/g, "")     // keep only ASCII-safe chars
    .replace(/\s+/g, "_")
    .slice(0, 100);
}


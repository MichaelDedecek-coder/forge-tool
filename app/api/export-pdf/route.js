// ────────────────────────────────────────────────────────────────
//  SVG Chart generators (static, suitable for PDF)
// ────────────────────────────────────────────────────────────────

function generateChartHTML(chart, index) {
  const chartType = chart.chartType || chart.type || "bar";
  const data = chart.data || [];
  const title = chart.title || `Chart ${index + 1}`;

  if (chartType === "bar") return generateBarChartSVG(title, data, chart);
  if (chartType === "line") return generateLineChartSVG(title, data, chart);
  if (chartType === "pie") return generatePieChartSVG(title, data, chart);

  return `<div class="chart-container"><h3>${escapeHtml(title)}</h3><p>Chart visualization</p></div>`;
}

function generateBarChartSVG(title, data, chart) {
  if (!data || data.length === 0) return "";

  const dataKeys = chart.dataKeys || [
    { name: Object.keys(data[0])[0], value: Object.keys(data[0])[1] },
  ];
  const nameKey = dataKeys[0].name;
  const valueKey = dataKeys[0].value;

  const maxValue = Math.max(...data.map((d) => parseFloat(d[valueKey]) || 0));
  const barWidth = 600 / data.length - 20;
  const colors = ["#3b82f6", "#10b981", "#8b5cf6", "#f59e0b", "#ef4444"];

  const bars = data
    .map((item, index) => {
      const value = parseFloat(item[valueKey]) || 0;
      const height = (value / maxValue) * 200;
      const x = index * (barWidth + 20) + 40;
      const y = 250 - height;

      return `
      <rect x="${x}" y="${y}" width="${barWidth}" height="${height}" fill="${colors[index % colors.length]}" rx="4"/>
      <text x="${x + barWidth / 2}" y="270" text-anchor="middle" font-size="10" fill="#475569">${escapeHtml(String(item[nameKey]).slice(0, 15))}</text>
      <text x="${x + barWidth / 2}" y="${y - 5}" text-anchor="middle" font-size="10" font-weight="bold" fill="#1e293b">${value}</text>
    `;
    })
    .join("");

  return `
    <div class="chart-container">
      <h3>${escapeHtml(title)}</h3>
      <svg width="100%" height="300" viewBox="0 0 700 300">${bars}<line x1="30" y1="250" x2="670" y2="250" stroke="#cbd5e1" stroke-width="1"/></svg>
    </div>
  `;
}

function generateLineChartSVG(title, data, chart) {
  if (!data || data.length === 0) return "";

  const dataKeys = chart.dataKeys || [
    { name: Object.keys(data[0])[0], value: Object.keys(data[0])[1] },
  ];
  const valueKey = dataKeys[0].value;
  const nameKey = dataKeys[0].name;

  const maxValue = Math.max(...data.map((d) => parseFloat(d[valueKey]) || 0));
  const stepX = 600 / (data.length - 1 || 1);

  const points = data
    .map((item, index) => {
      const value = parseFloat(item[valueKey]) || 0;
      const x = index * stepX + 50;
      const y = 250 - (value / maxValue) * 200;
      return `${x},${y}`;
    })
    .join(" ");

  const labels = data
    .map((item, index) => {
      const x = index * stepX + 50;
      return `<text x="${x}" y="270" text-anchor="middle" font-size="10" fill="#475569">${escapeHtml(String(item[nameKey]).slice(0, 15))}</text>`;
    })
    .join("");

  const dots = data
    .map((item, index) => {
      const value = parseFloat(item[valueKey]) || 0;
      const x = index * stepX + 50;
      const y = 250 - (value / maxValue) * 200;
      return `<circle cx="${x}" cy="${y}" r="4" fill="#3b82f6"/>`;
    })
    .join("");

  return `
    <div class="chart-container">
      <h3>${escapeHtml(title)}</h3>
      <svg width="100%" height="300" viewBox="0 0 700 300">
        <polyline points="${points}" fill="none" stroke="#3b82f6" stroke-width="2"/>${dots}${labels}
        <line x1="40" y1="250" x2="660" y2="250" stroke="#cbd5e1" stroke-width="1"/>
      </svg>
    </div>
  `;
}

function generatePieChartSVG(title, data, chart) {
  if (!data || data.length === 0) return "";

  const dataKeys = chart.dataKeys || [
    { name: Object.keys(data[0])[0], value: Object.keys(data[0])[1] },
  ];
  const valueKey = dataKeys[0].value;
  const nameKey = dataKeys[0].name;

  const total = data.reduce(
    (sum, item) => sum + (parseFloat(item[valueKey]) || 0),
    0
  );
  const colors = ["#3b82f6", "#10b981", "#8b5cf6", "#f59e0b", "#ef4444"];

  let currentAngle = -90;
  const slices = data
    .map((item, index) => {
      const value = parseFloat(item[valueKey]) || 0;
      const angle = (value / total) * 360;

      const startAngle = currentAngle;
      const endAngle = currentAngle + angle;
      currentAngle = endAngle;

      const startRad = (startAngle * Math.PI) / 180;
      const endRad = (endAngle * Math.PI) / 180;

      const x1 = 150 + 80 * Math.cos(startRad);
      const y1 = 150 + 80 * Math.sin(startRad);
      const x2 = 150 + 80 * Math.cos(endRad);
      const y2 = 150 + 80 * Math.sin(endRad);

      const largeArc = angle > 180 ? 1 : 0;

      return `<path d="M 150 150 L ${x1} ${y1} A 80 80 0 ${largeArc} 1 ${x2} ${y2} Z" fill="${colors[index % colors.length]}"/>`;
    })
    .join("");

  const legend = data
    .map((item, index) => {
      return `
      <div style="display: flex; align-items: center; margin: 4px 0;">
        <div style="width: 12px; height: 12px; background: ${colors[index % colors.length]}; margin-right: 8px; border-radius: 2px;"></div>
        <span style="font-size: 9pt; color: #475569;">${escapeHtml(String(item[nameKey]))} (${((parseFloat(item[valueKey]) / total) * 100).toFixed(1)}%)</span>
      </div>
    `;
    })
    .join("");

  return `
    <div class="chart-container">
      <h3>${escapeHtml(title)}</h3>
      <div style="display: flex; align-items: center; justify-content: space-around;">
        <svg width="300" height="300" viewBox="0 0 300 300">${slices}</svg>
        <div>${legend}</div>
      </div>
    </div>
  `;
}

// Escape HTML to prevent XSS
function escapeHtml(text) {
  const map = {
    "&": "&amp;",
    "<": "&lt;",
    ">": "&gt;",
    '"': "&quot;",
    "'": "&#039;",
  };
  return String(text).replace(/[&<>"']/g, (m) => map[m]);
}
